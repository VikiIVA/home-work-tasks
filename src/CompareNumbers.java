public class CompareNumbers {
    public static void main(String[] args) {
        int a = 5;
        int b = 8;

        if (a > b) {
            System.out.println("The number " + a + " is the greatest value");
        }
        else if (b > a) {
            System.out.println("The number " + b + " is the greatest value");
        }
        else {
            System.out.println("The numbers are equal");
        }
    }
}
